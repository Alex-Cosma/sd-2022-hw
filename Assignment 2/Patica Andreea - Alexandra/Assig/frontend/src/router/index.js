import Vue from "vue";
import VueRouter from "vue-router";
import UserList from "../views/UserList.vue";
import AdminItemList from "../views/ItemList.vue";
import ItemList from "../views/ItemListEmployee.vue";
import { auth as store } from "../store/auth.module";
import Login from "../views/Login";
import AdminOpts from "../views/AdminOptions.vue";
import GoogleBooksApi from "../views/GoogleApi/GoogleBooksApi.vue"

Vue.use(VueRouter);

const routes = [
  {
    path: "/",
    name: "Login",
    component: Login,
  },
  {
    path: "/options",
    name: "AdminOpts",
    component: AdminOpts,
    beforeEnter: (to, from, next) => {
      if (store.getters.isAdmin) {
        next();
      } else {
        next({ name: "Items" });
      }
    },
  },
  {
    path: "/users",
    name: "UserList",
    component: UserList,
    beforeEnter: (to, from, next) => {
      if (store.getters.isAdmin) {
        next();
      } else {
        next({ name: "Items" });
      }
    },
  },
  {
    path: "/admin-items",
    name: "AdminItems",
    component: AdminItemList,
    beforeEnter: (to, from, next) => {
      if (store.getters.isAdmin) {
        next();
      } else {
        next({ name: "Items" });
      }
    },
  },
  {
    path: "/google-books",
    name: "GoogleBooksApi",
    component: GoogleBooksApi,
    beforeEnter: (to, from, next) => {
      if (store.getters.isAdmin) {
        next();
      } else {
        next({ name: "Items" });
      }
    },
  },
  {
    path: "/items",
    name: "Items",
    component: ItemList,
    beforeEnter: (to, from, next) => {
      if (store.state.status.loggedIn) {
        next();
      } else {
        next({ name: "Home" });
      }
    },
  },
  {
    path: "/about",
    name: "About",
    // route level code-splitting
    // this generates a separate chunk (about.[hash].js) for this route
    // which is lazy-loaded when the route is visited.
    component: () =>
      import(/* webpackChunkName: "about" */ "../views/About.vue"),
  },
];

const router = new VueRouter({
  routes,
});

export default router;
